#arthmetic operators
#addition +
x=20
y=30
print(f"The sum of {x} and {y} is {x+y}")
#subtraction -
print("The difference between",y,"and",x ,"is",y-x)
#multiplication *
print(f"The product of {x} and {y} is {x*y}")
#divison /
print(f"The divison of {y} and {x} is {y/x}")
#modulus=% the remainder of division
print(f"the modulus of {y} and {x} is {y%x}")
#power **
print(f"{x} power 3 is {x**3}")
#camparison operations
#greater than>21
print(f"is {x} greater than {y}? {x>y}")
#less than <
print(f"is {x} less than {y}? {x<y}")
#equal to ==
print(f"is {x} equal than {y}? {x==y}")
#greater than or equal to >=
print(f"is {x} greater than or equal to {y}? {x>=y}")
#less than or equal to <=
print(f"is {x} less than or equal to {y}? {x<=y}")
#notequal to !=
print(f"is {x} not equal to {y}? {x!=y}")
#logical operators and, or and not
#and-returns if both statements are true
z=10
print(z<20 and z>5)
print(z>20 and z<5)
#or-returns true if one statement is true
print(z>20 or z>5)
#not-returns the reverse
print(not(z>5))
#assignment operators =,+=,-=,*=
b=40
print(b)
#+=
b+=5
print(b)
#-=
b-=3
print(b)
